  static String t(
    String language,
    String key, {
    Map<String, String> args = const {},
  }) {
    final lang = _strings[language] ?? _strings[fallbackLanguage];
    var value = lang?[key] ?? _strings[fallbackLanguage]?[key] ?? key;

    args.forEach((placeholder, replacement) {
      value = value.replaceAll('{$placeholder}', replacement);
    });

    return value;
  }
