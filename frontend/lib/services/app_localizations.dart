import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

import 'token_storage.dart';

class AppLanguage extends ChangeNotifier {
  // Holds current language code and notifies listeners on change.
  AppLanguage({String initialCode = 'en'}) : _code = initialCode;

  String _code;

  String get code => _code;

  Future<void> loadFromStorage() async {
    final profile = await const TokenStorage().readUserProfile();
    final preferred = profile?['preferred_language']?.toString();
    setLanguage(preferred);
  }

  void setLanguage(String? languageCode) {
    if (languageCode == null || languageCode.isEmpty) {
      return;
    }
    if (!AppLocalizations.supportedLanguages.contains(languageCode)) {
      return;
    }
    if (_code == languageCode) {
      return;
    }
    _code = languageCode;
    notifyListeners();
  }
}

