extension LocalizationExtension on BuildContext {
  String t(String key, {Map<String, String> args = const {}}) {
    final language = watch<AppLanguage>().code;
    return AppLocalizations.t(language, key, args: args);
  }

  String tRead(String key, {Map<String, String> args = const {}}) {
    final language = read<AppLanguage>().code;
    return AppLocalizations.t(language, key, args: args);
  }
